  var lang = localStorage.getItem('ls_lang') || 'fr';

  function t(key, vars) {
    var val = (dict[lang] && dict[lang][key]) || key;
    if (vars) {
      for (var k in vars) {
        val = val.replace('{' + k + '}', vars[k]);
      }
    }
    return val;
  }

  function refreshPage() {
    document.documentElement.lang = lang;
    applyAll();
    window.dispatchEvent(new CustomEvent('i18n:change', { detail: { lang: lang } }));
  }

  function setLang(l) {
    if (dict[l]) {
      lang = l;
      localStorage.setItem('ls_lang', l);
      refreshPage();
      var btn = document.getElementById('lang-toggle');
      if (btn) btn.textContent = t('nav.lang');
    }
  }

  function getLang() {
    return lang;
  }

  function applyAll() {
    document.querySelectorAll('[data-i18n]').forEach(function(el) {
      var key = el.getAttribute('data-i18n');
      var icon = el.querySelector('i,svg');
      if (icon) {
        Array.from(el.childNodes).forEach(function(node) {
          if (node.nodeType === Node.TEXT_NODE) node.remove();
        });
        el.appendChild(document.createTextNode(' ' + t(key)));
      } else {
        el.textContent = t(key);
      }
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(function(el) {
      var key = el.getAttribute('data-i18n-placeholder');
      el.setAttribute('placeholder', t(key));
    });
    document.querySelectorAll('[data-i18n-aria-label]').forEach(function(el) {
      var key = el.getAttribute('data-i18n-aria-label');
      el.setAttribute('aria-label', t(key));
    });
    document.querySelectorAll('[data-i18n-title]').forEach(function(el) {
      var key = el.getAttribute('data-i18n-title');
      el.setAttribute('title', t(key));
    });
  }

  function mountToggle(btnId) {
    var btn = document.getElementById(btnId);
    if (!btn) return;

    if (btn.dataset.langToggleBound === 'true') {
      btn.textContent = t('nav.lang');
      return;
    }

    btn.dataset.langToggleBound = 'true';
    btn.textContent = t('nav.lang');
    btn.addEventListener('click', function(e) {
      e.preventDefault();
      setLang(lang === 'fr' ? 'en' : 'fr');
    });
  }

